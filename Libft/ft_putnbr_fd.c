#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	long	num;

	i = 0;
	num = n;
	if (num < 0)
	{
		write(fd, "-", 1);
		num = -num;
	}
	if (num > 9)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + 48, fd);
}
