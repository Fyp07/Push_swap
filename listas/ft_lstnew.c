#include "../push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*lst;

	lst = malloc(sizeof(t_list));
	if (!lst)
		return (NULL);
	lst->value = content;
	lst->next = NULL;
	lst->previous = NULL;
	return (lst);
}
